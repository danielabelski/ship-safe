// ── Route handler ──────────────────────────────────────────────────────────

export async function POST(req: NextRequest) {
  const session = await auth();
  if (!session?.user?.id) {
    return NextResponse.json({ error: 'Unauthorized' }, { status: 401 });
  }

  let config: HermesConfig;
  try {
    config = await req.json();
  } catch {
    return NextResponse.json({ error: 'Invalid request body' }, { status: 400 });
  }

  // Validate
  if (!config.tools || !Array.isArray(config.tools) || config.tools.length === 0) {
    return NextResponse.json({ error: 'At least one tool is required' }, { status: 400 });
  }
  if (config.tools.length > 50) {
    return NextResponse.json({ error: 'Too many tools (max 50)' }, { status: 400 });
  }

  // Sanitize tool names — only allow safe identifiers
  config.tools = config.tools.map(t => ({
    name: t.name.replace(/[^a-zA-Z0-9_-]/g, '_').slice(0, 64),
    sourceUrl: t.sourceUrl?.startsWith('https://') ? t.sourceUrl.slice(0, 512) : undefined,
  }));

  // Generate all files
  const zip = new JSZip();

  // agent-manifest.json
  const manifestPath = config.manifestPath || 'agent-manifest.json';
  zip.file(manifestPath, generateAgentManifest(config));

  // .ship-safe/agents/hermes-policy.js
  zip.file('.ship-safe/agents/hermes-policy.js', generateHermesPolicy(config));

  // .ship-safe/hermes-baseline.json
  zip.file('.ship-safe/hermes-baseline.json', generateBaseline(config));

  // CI config
  if (config.ciProvider === 'github') {
    zip.file('.github/workflows/ship-safe-hermes.yml', generateGitHubWorkflow(config));
  } else if (config.ciProvider === 'gitlab') {
    zip.file('ship-safe-hermes-ci.yml', generateGitLabCI(config));
  }

  // Setup guide
  zip.file('SHIP_SAFE_SETUP.md', generateSetupGuide(config));

  const zipBuffer = await zip.generateAsync({ type: 'nodebuffer', compression: 'DEFLATE' });

  const projectSlug = (config.projectName || 'hermes-project')
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, '-')
    .replace(/^-|-$/g, '')
    .slice(0, 40);

  return new NextResponse(zipBuffer as unknown as BodyInit, {
    status: 200,
    headers: {
      'Content-Type': 'application/zip',
      'Content-Disposition': `attachment; filename="ship-safe-hermes-${projectSlug}.zip"`,
      'Content-Length': String(zipBuffer.length),
    },
  });
}
